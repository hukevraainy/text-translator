function randomizeText(text, style) {
    let result = '';
    words = text.split(/(\s+)/);
    for (const word of words){
        if (/\s/.test(word)) {
            result += word; // If it's whitespace, add it unchanged
        }

        processedword = '';
        for (const char of word) {
        if (charMappings[char]) {
            const variants = charMappings[char];
            result += variants[Math.floor(Math.random() * variants.length)];
            continue;
        }
    
        // Check lowercase version
        const lowerChar = char.toLowerCase();
        if (charMappings[lowerChar]) {
            const variants = charMappings[lowerChar];
            const randomVariant = variants[Math.floor(Math.random() * variants.length)];
            result += (char === char.toUpperCase()) 
                ? randomVariant.toUpperCase() 
                : randomVariant;
            continue;
        }

        processedword += char; 
    }

    result += processedword;
    }

    if (style == 'cute' || style == 'evil') {
        const styleChars = charMappings[style];
        const stickerCount = Math.floor(Math.random() * 5) + 1;
        for (let i = 0; i < stickerCount; i++) {
            result += ' ' + styleChars[Math.floor(Math.random() * styleChars.length)];
        }
    }
    return result;
}

document.getElementById('randomizeBtn').addEventListener('click', function() {
    const inputText = document.getElementById('inputText').value;
    const outputElement = document.getElementById('output');
    
    if (!inputText.trim()) {
        outputElement.textContent = "Please enter some text to randomize!";
        return;
    }
    
    // For this example, we'll use the 'fancy' style
    const selectedstyle = styleSelector.value
    const randomizedText = randomizeText(inputText, selectedstyle);
    outputElement.textContent = randomizedText;
});

